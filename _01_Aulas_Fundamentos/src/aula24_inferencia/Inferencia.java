package aula24_inferencia;

public class Inferencia {

	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
		
		var b = 4.5;
		System.out.println(b);
		
		var c = "Paulo";
		System.out.println(c);

		c = "Paulo Cardoso";
		System.out.println(c);				
		
		double d;  // Decladara
		d = 125.55;  // Inicializada
		System.out.println(d);  // usada					
	}
}
