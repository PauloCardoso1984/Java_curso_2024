package aula25_Tipos_Pimitivos;

public class Primitivo1 {

	public static void main(String[] args) {
		
		// Valores numéricos
		Byte a = 1;					// 1 byte
		Short b = 10;  				// 2 byte 
		Integer c = 1001;  			// 4 byte 
		Long d = 100000000000L; 	// 8 byte 
		Float e = 10000000000F; 	// 4 byte
		Double f = 10000000.0;  	// 8 byte
		
		// Valores especifico
		char g = 'S';
		Boolean h = true;
		
		// Atividades - TIPOS de numeriocos
		byte anosDeEmpresa = 23;
		short numerosDeVoos = 327;
		int id = 56789;
		long pontosAcumulados = 3_324_854_213L;
		
		
		// Tipos numéricos reais
		float salario = 11_456.58F;
		double vendasAcumuladas = 2_454_897_852.55;
		
		// Tipo Boleano
		boolean estaDeFerias = false;
		
		// Tipo Caractere
		char status = '?';
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(anosDeEmpresa);
		System.out.println(numerosDeVoos);
		System.out.println(id);
		System.out.println(pontosAcumulados);
		System.out.println(salario);
		System.out.println(vendasAcumuladas);
		System.out.println(estaDeFerias);
		System.out.println(status);
		
	}
}
